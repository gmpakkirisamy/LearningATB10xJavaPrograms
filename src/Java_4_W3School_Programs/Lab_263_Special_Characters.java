package Java_4_W3School_Programs;

public class Lab_263_Special_Characters {
    public static void main(String[] args) {
        String sentence = " The is \"very\" long sentence in this page";
        //To print "" quotes you have to use the backslash \
        System.out.println(sentence);

        String sentence2 = "This is the final's output ";
        System.out.println(sentence2);

        String sentence3 = "The character \\ is called backslash.";
        // This will print the single slash.
        System.out.println(sentence3);
    }
}
