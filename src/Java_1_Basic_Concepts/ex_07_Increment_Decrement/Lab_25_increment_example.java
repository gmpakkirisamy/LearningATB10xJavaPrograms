package Java_1_Basic_Concepts.ex_07_Increment_Decrement;

public class Lab_25_increment_example {
    public static void main(String[] args) {
        int a =56;
        int result = a++; //Post increment concept
        System.out.println(a); // It will print the incremented value of a
        System.out.println(result); // It will print the older a value first
    }
}
