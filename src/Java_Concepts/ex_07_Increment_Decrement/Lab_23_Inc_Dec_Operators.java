package Java_Concepts.ex_07_Increment_Decrement;

public class Lab_23_Inc_Dec_Operators {
    public static void main(String[] args) {

        //Pre Increment - ++a - It will increment the value then print
        //Post Increment - a++ - It will print the value then it will print.
        int a = 10;
        int b = ++a;
        System.out.println(b);



    }
}
