package Java_Concepts.ex_11_while_loop;

public class Lab_47_while_loop_example {
    public static void main(String[] args) {
       // This is called as entry controlled loop. Only i will execute th loop if condition is satisfied.
        int i=0;
        while(i<10){
            System.out.println(i);
            i++;
        }
    }
}
