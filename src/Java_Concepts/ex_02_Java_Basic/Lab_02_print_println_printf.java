public class Lab_02_print_println_printf {
    public static void main(String[] args) {
        //println - print in successive line or print in the new line
        //print - print in the single line
        //printf- It will replace the value of %
        int a =10;
        System.out.printf("The variable value is %d", a);
        //System.out.println(a);
        //%d is for int, byte, long, short
        //%s is for character
        //%f is for float and double
        //%b is for boolean
    }
}
