package Java_Basic_Concepts.ex_03_Literals;

public class Lab_10_Operators {
    public static void main(String[] args) {

        //Assignment operators
        //=  is the assignment operators

        //Arithmetic Operators
        int a=20;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);  // Print the quotinet values
        System.out.println(a%b);  // Print the reminder values (Mod)

        // Unary Operator - Usualy with single operand with single values
        int c = +10;
        int d = -10;
        System.out.println(c);
        System.out.println(d);
    }
}
